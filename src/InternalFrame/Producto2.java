                            parametrosProducto = "'" + txtDescripcion.getText() + "','" + txtNombre.getText() + "'," + txtPrecio.getText() + "," + labelCostoProduccion.getText();

                    nuevoRegistro(colummnasProducto, parametrosProducto, "producto");
