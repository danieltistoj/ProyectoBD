    private void clickPaneles(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_clickPaneles
        if (evt.getSource() == pnlBuscarArchivo) {
            JFileChooser ch = new JFileChooser();
            FileNameExtensionFilter file = new FileNameExtensionFilter("SQL", "sql");
            ch.setFileFilter(file);

            int se = ch.showOpenDialog(null);
            if (se == JFileChooser.APPROVE_OPTION) {
                String ruta = ch.getSelectedFile().getPath();
                txtRupaImportar.setText(ruta);
            }
        }
        if (evt.getSource() == pnlImportarBase) {
            boolean existe = false;
            if (!"".equals(txtRupaImportar.getText())) {
                String NuevaBase = JOptionPane.showInputDialog("Escriba el nombre de la base de datos");
                if (NuevaBase != null) {
                    if (NuevaBase.length() != 0 && !"".equals(NuevaBase)) {
                        if (modelo.existeLaBase(NuevaBase)) {
                            JOptionPane.showMessageDialog(null, "La base de datos ya existe", "Error", JOptionPane.ERROR_MESSAGE);
                        } else {
                            conexion.conexionMySQL.EjecutarInstruccion("create database " + NuevaBase);
                            importar.ImportarBase(txtRupaImportar.getText(), NuevaBase);
                            fichero.setFicheroBase(NuevaBase);
                            conexion = new VariableGlobal();
                            txtRupaImportar.setText("");
                            int resp = JOptionPane.showConfirmDialog(null,"¿Desea reiniciar la el programa","",JOptionPane.YES_NO_OPTION);
                            if(resp == 0){
                                Cargador cargar = new Cargador();
                                cargar.setVisible(true); 
                                dispose();
                            }
                            else{
                                System.exit(0);
                            }

                        }
                    } else {
                        JOptionPane.showMessageDialog(null, "Ingrese un nombre para la base de datos", "Error", JOptionPane.ERROR_MESSAGE);
                    }
                }
            } else {
                JOptionPane.showMessageDialog(null, "Ingrese un archivo", "Error", JOptionPane.ERROR_MESSAGE);
            }
        }
    }//GEN-LAST:event_clickPaneles

