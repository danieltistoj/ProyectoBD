    private void botonAceptarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_botonAceptarActionPerformed
        String contra,parametros,datos;
        int confirmar;
        if(txtNombre.getText().length()>0&&txtContra.getText().length()>0){
            confirmar = JOptionPane.showConfirmDialog(null,"Â¿Esta seguro con los datos que ingreso?","Confirmar",JOptionPane.YES_NO_OPTION);
            if(confirmar == 0){
            contra = DigestUtils.md5Hex(txtContra.getText());
            parametros = "usuario,password,nombre,correo,telefono,celular,dpi,tipo";
            datos = "'Administrador','"+contra+"','"+txtNombre.getText()+"','"+txtCorreo.getText()+"','"+txtTelefono.getText()+"','"+txtCelular.getText()+"','"+txtDPI.getText()+"',"+1;
            primero.nuevoRegistro(parametros,datos,"usuario");
            JOptionPane.showMessageDialog(null,"Primer usurio ingresado con exito");
            
            this.dispose();
            InicioSesion inicio = new InicioSesion();
            inicio.setVisible(true);
            
            }
        }
        else{
            JOptionPane.showMessageDialog(null,"Llene los campos obligatorios","Advertencia",JOptionPane.WARNING_MESSAGE);
        }
    }//GEN-LAST:event_botonAceptarActionPerformed

