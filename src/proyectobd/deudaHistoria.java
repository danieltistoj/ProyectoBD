    private void comoboCargarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_comoboCargarActionPerformed
        if(comoboCargar.getSelectedIndex() == 0){
           //desactivar modulo cargar por rango de fecha
           botonCragarFecha.setEnabled(false);
           txtFechaF.setText("");
           txtFechaI.setText("");
           
           txtFechaF.setEnabled(false);
           txtFechaI.setEnabled(false);
           //activar modulo cargar por id o numero
           botonCargarId.setEnabled(true);
           txtId.setEnabled(true);
        }
        else{
            //desactivar modulo id o numero
            botonCargarId.setEnabled(false);
            txtId.setEnabled(false);
            txtId.setText("");
            //activar boton cargar rango de fecha 
             botonCragarFecha.setEnabled(true);
             txtFechaF.setEnabled(true);
             txtFechaI.setEnabled(true);
        }
    }//GEN-LAST:event_comoboCargarActionPerformed

